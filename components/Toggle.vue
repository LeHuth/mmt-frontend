<template>
    <div class="orta">
      <input id="karanlık" class="lamba" type="checkbox" aria-label="karanlık" @click="toggleDarkMode">
    </div>
  </template>

  <script setup lang="ts">
  import { onMounted, ref } from 'vue';

  const toggleDarkMode = () => {
    document.documentElement.classList.toggle('karanlık');

    if (document.documentElement.classList.contains('karanlık')) {
      localStorage.setItem('gmtNightMode', 'true');
    } else {
      localStorage.removeItem('gmtNightMode');
    }
  };

  const ahmet = ref(null);

  onMounted(() => {
    const codescaptain = localStorage.getItem('gmtNightMode');
    const checkbox = document.querySelector('#karanlık');

    if (codescaptain) {
      document.documentElement.classList.add('karanlık');
      checkbox.checked = true;
    }

    ahmet.value = checkbox;
  });
  </script>

